float read_ads1115(void){
	buffer[0]=0x01;//config reg(read&write) was choosed for pointer. frame2
	buffer[1]=0XC2; //frame3
	buffer[2]=0x85;//or 83, frame4
	HAL_I2C_Master_Transmit(&hi2c1,addr,(uint8_t*)buffer,3,100);
	//We can write 0x48<<1 for adress  
	//read 
	buffer[0]=0x00;//conversion reg(only read) was choosed
	HAL_I2C_Master_Transmit(&hi2c1,addr,(uint8_t*)buffer,1,100);
	HAL_I2C_Master_Receive(&hi2c1,addr+1,(uint8_t*)buffer,2,100);
	data=(buffer[0]<<8)+buffer[1]; // we can use "+" or "|"
	return (float)0.125*(float)data; //+-4095 was choosed; 4096*2=8192; it is mapping 16 bite register, max deger for reg  2^16=65536;
// 8096/65536=0.125;
}


